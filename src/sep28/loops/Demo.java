package sep28.loops;

public class Demo {
    public static void main(String[] args) {

        // break     : break the iteration
        // continue  : skip the iteration

        for (int i = 0; i < 10; i++) {
            if(i == 3){
                continue;
            }
            System.out.println(i);
        }
    }
}


